public class Position {
    private int row;
    private int col;

    Position(int row,int col){
        this.col=col;
        this.row=row;
    }

    public void setPosition(int row,int col){
        this.col=col;
        this.row=row;
    }

    public int getRow(){
        return this.row;
    }
    public int getCol(){
        return this.col;
    }
    

}
